#fungsi abs
print (abs(-3))


#fungsi ceiling
import math #cukup sekali dalam satu file text editor
bilangan1 = 5.3
bilangan2 = 5.6
print(math.ceil(bilangan1))
print(math.ceil(bilangan2))

#fungsi floor
print (math.floor(bilangan1))
print (math.floor(bilangan2))

#fungsi Log
print(math.log(10))
print(math.log(10,3))

#fungsi max dan min
a = [-40, -50, 100, 35, 50]
print(a)
print("Nilai terbesar: ", max(a))
print("Nilai terkecil: ", min(a))

#fungsi pow
pow(4, 3)

#fungsi round
print(round(bilangan1)) #bilangan1 =5.3
print(round(bilangan2)) #bilangan2 =5.6


