x = 5
y = 3

#sama dengan (simbolnya ==)
print (x == y) #output: False, karena 5 tidak sama dengan 3

#tidak sama dengan (simbolnya !=)
print (x!=y) #output: True, karena 5 memang tidak sama dengan 3

#Bagaimana penggunaan source code jika < dan>?

#tidak lebih kecil(simbolnya <)
print(x<y) #output: False, karena 5 tidak lebih kecil dari 3
#lebih besar(simbolnya >)
print(x>y) #output: True, karena 5 lebih besar dari 3
