#mencetak karakter
print ("Naufal Athaya Khansa") #menampilkan output"Naufal Athaya Khansa"

#mencetak angka/integer #menampilkan output angka/integer"1234"
print (1234)

#mencetak karakter dan angka
print ("Naufal Athaya Khansa", 1234) #menampilkan output string dan angka/integer sekaligus

#tipe data boolean
print(True) #menampilkan output boolean"True"
#tipe data string 
print("ayo kita belajar") #menampilkan output berupa string"ayo kita belajar"
#tipe data integer 
print(20) #menampilkan output integer angka
#tipe data float
print(2.5) #menampilkan output data float(decimal)
#tipe data hexadecimal
print(0x9a) #menampilkan output hexadecimal 0x9a(154 dalam decimal)
#tipe data complex
print(5j) #menampilkan output data komplek imajiner(j)
#type data list
print([1,"dua",3,"empat",5]) #menampilkan campuran data(string dan integer)(dapat diubah)
#tipe data tuple
print((1,"dua",3,"empat",5))#menampilkan campuran data(string dan integer)( tidak dapat diubah)
#tipe data dictionary
print({"nama":"Naufal", 'umur':"20"}) #menampilkan output pasangan penunjuk dan variabel
#tipe data dictionary dimasukan kedalam variable biodata 
biodata = {"nama":"Naufal", 'umur':"20"} #proses inialisasi variabel biodata
print(biodata) #menampilkan output variable biodata
#proses pencetakan variable biodata yang berisi tipe data dictionary
print(type(biodata)) #mencetak tipe data variabel
"""fungsi untuk mengecek jenis tipe data yang akan tampil <class 'dict'>
yang berarti dict adalah tipe data dictionary""" #komentar